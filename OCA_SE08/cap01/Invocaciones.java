package cap01;

public class Invocaciones {
    
    public static void main(String ... args){
        System.out.println(":::  ejecutando ....");
    }

    public static void main(String argumentos){
        System.out.println(":::  ejecutando main ..");
    }

    public static void main(int args){
        System.out.println(":::  ejecutando main ..");
    }
}
